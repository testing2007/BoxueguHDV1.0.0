import Foundation

class BXGNavLogoItem: UIBarButtonItem {
    
    class func item() -> UIBarButtonItem {
        
        let imageView = UIImageView(image: #imageLiteral(resourceName: "navigationbar_logo"))
        return UIBarButtonItem(customView: imageView)
    }
    
}
